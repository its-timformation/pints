export type LoadingSurface = string; // kept for backwards compatibility

export function loadingMessage(_surface?: LoadingSurface, seed?: number): string {
  const i = seed != null
    ? Math.abs(seed) % LOADING_MESSAGES.length
    : Math.floor(Math.random() * LOADING_MESSAGES.length);
  return LOADING_MESSAGES[i];
}
